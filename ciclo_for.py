# Ciclo for ejecuta un bloque de código un número determinado de veces, a diferencia del ciclo while que se ejecuta mientras se cumpla una condición.
print("Ciclo For")
cadena = "Hola Mundo"
for letra in cadena:
  print(letra, end=" ")

print("\n\nRecorriendo una lista de Frutas")
frutas = ["Manzana", "Pera", "Naranja"]
for fruta in frutas:
  print(fruta, end= " ")